public class VariablesAndDatatypes {
    public static void main(String[] args){
        int i = 45;
        float f = 4.234f;
        String s = "Hello World";
        char c = 'c';
        boolean is = false;
        System.out.println(i);
        System.out.println(f);
        System.out.println(s);
        System.out.println(c);
        System.out.println(is);
    }
}
